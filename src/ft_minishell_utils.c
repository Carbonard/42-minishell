#include "ft_minishell.h"

int	is_metachar(char c)
{
	if (c == '<' || c == '>' || c == '|' || c == '&' || c == ' ' || c == '\t'
		|| c == '\n' || c == '(' || c == ')' || c == ';')
		return (1);
	return (0);
}

int	is_redirection(char *str)
{
	if ((str[0] == '<' && str[1] == '<') || (str[0] == '>' && str[1] == '>'))
		return (2);
	if (str[0] == '<' || str[0] == '>')
		return (1);
	return (0);
}
